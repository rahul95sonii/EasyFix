package com.easyfix.util.triggers.model;

public class Tracking
{
	
	private int countJobCreated;
	private int countJobScheduled;
	private int countJobCheckedout;
	private int clientShare;
	private int countJobRequested;
	public int getCountJobCreated()
	{
		return countJobCreated;
	}
	public void setCountJobCreated(int countJobCreated)
	{
		this.countJobCreated = countJobCreated;
	}
	public int getCountJobScheduled()
	{
		return countJobScheduled;
	}
	public void setCountJobScheduled(int countJobScheduled)
	{
		this.countJobScheduled = countJobScheduled;
	}
	public int getCountJobCheckedout()
	{
		return countJobCheckedout;
	}
	public void setCountJobCheckedout(int countJobCheckedout)
	{
		this.countJobCheckedout = countJobCheckedout;
	}
	public int getClientShare()
	{
		return clientShare;
	}
	public void setClientShare(int clientShare)
	{
		this.clientShare = clientShare;
	}
	public int getCountJobRequested()
	{
		return countJobRequested;
	}
	public void setCountJobRequested(int countJobRequested)
	{
		this.countJobRequested = countJobRequested;
	}
	
	
}
