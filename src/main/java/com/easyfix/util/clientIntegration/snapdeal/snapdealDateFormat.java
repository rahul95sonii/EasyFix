package com.easyfix.util.clientIntegration.snapdeal;

public class snapdealDateFormat {

	private Integer year;//"year":2016,

	private Integer month;// "month":7,

	private Integer day;//"day":18,

	private Integer hour;//"hour":11,

	private Integer minute;//"minute":45

	public Integer getYear() {
		return year;
	}

	public Integer getMonth() {
		return month;
	}

	public Integer getDay() {
		return day;
	}

	public Integer getHour() {
		return hour;
	}

	public Integer getMinute() {
		return minute;
	}

	public void setYear(Integer year) {
		this.year = year;
	}

	public void setMonth(Integer month) {
		this.month = month;
	}

	public void setDay(Integer day) {
		this.day = day;
	}

	public void setHour(Integer hour) {
		this.hour = hour;
	}

	public void setMinute(Integer minute) {
		this.minute = minute;
	}
	
	
}
